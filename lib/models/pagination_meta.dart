class PaginationMeta {
  final int pageNo;
  final int pageSize;
  final int totalPages;

  PaginationMeta({
    required this.pageNo,
    required this.pageSize,
    required this.totalPages,
  });

  factory PaginationMeta.fromJson(Map json) {
    return PaginationMeta(
      pageNo: int.parse("${json['meta']['currentPage']}"),
      pageSize: int.parse("${json['meta']['perPage']}"),
      totalPages: int.parse("${json['meta']['lastPage']}"),
    );
  }
}
